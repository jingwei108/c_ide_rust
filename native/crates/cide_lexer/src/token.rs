//! Lexer 公共类型定义。

/// 词法单元类型。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TokenType {
    Int,
    Void,
    Char,
    If,
    Else,
    While,
    Do,
    For,
    Return,
    Break,
    Continue,
    Struct,
    Union,
    Sizeof,
    Offsetof,
    Switch,
    Case,
    Default,
    Typedef,
    Enum,
    Unsigned,
    Long,
    Short,
    Signed,
    Const,
    Extern,
    Float,
    Double,
    Volatile,
    Inline,
    Restrict,
    Register,
    Auto,
    Bool,
    Goto,
    Null,
    // === C++ 新增关键字 ===
    Class,
    Public,
    Private,
    Protected,
    This,
    Using,
    Namespace,
    Virtual,
    Override,
    Explicit,
    Friend,
    Template,
    Typename,
    StaticCast,
    ConstCast,
    ReinterpretCast,
    New,
    Delete,
    // === C++ 新增标点 ===
    ColonColon,
    ArrowStar,
    DotStar,
    Identifier,
    Number,
    UnsignedLiteral,
    FloatLiteral,
    LongLiteral,
    CharLiteral,
    String,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Eq,
    Ne,
    Lt,
    Le,
    Gt,
    Ge,
    AndAnd,
    OrOr,
    Not,
    Assign,
    PlusAssign,
    MinusAssign,
    StarAssign,
    SlashAssign,
    PercentAssign,
    AndAssign,
    OrAssign,
    XorAssign,
    ShlAssign,
    ShrAssign,
    Ampersand,
    BitOr,
    BitXor,
    BitNot,
    Shl,
    Shr,
    Increment,
    Decrement,
    Semicolon,
    Comma,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Dot,
    Arrow,
    Colon,
    Question,
    Eof,
    Unknown,
}

/// 词法单元。
#[derive(Debug, Clone)]
pub struct Token {
    pub ty: TokenType,
    pub text: String,
    pub line: i32,
    pub column: i32,
}

/// 词法错误。
#[derive(Debug, Clone)]
pub struct LexerError {
    pub message: String,
    pub line: i32,
    pub column: i32,
    pub code: i32,
}
