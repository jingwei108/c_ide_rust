#include <stdio.h>

int isValidSudoku(char* board) {
    int row[9][10];
    int col[9][10];
    int box[9][10];
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 10; j++) {
            row[i][j] = 0;
            col[i][j] = 0;
            box[i][j] = 0;
        }
    }
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 9; j++) {
            char c = board[i * 9 + j];
            if (c == '.') continue;
            int num = c - '0';
            int b = (i / 3) * 3 + j / 3;
            if (row[i][num] || col[j][num] || box[b][num]) return 0;
            row[i][num] = 1;
            col[j][num] = 1;
            box[b][num] = 1;
        }
    }
    return 1;
}

